# Penjumlahan
def penjumlahan(angka1, angka2)
    print "Penjumlahan dari #{angka1} dan #{angka2} adalah : "
    puts angka1 + angka2
end

penjumlahan(1, 5)

# Pengurangan
def pengurangan(angka1, angka2)
    print "Pengurangan dari #{angka1} dan #{angka2} adalah : "
    puts angka1 - angka2
end

pengurangan(1, 5)

# Perkalian
def perkalian(angka1, angka2)
    print "Perkalian dari #{angka1} dan #{angka2} adalah : "
    puts angka1 * angka2
end

perkalian(1, 5)

