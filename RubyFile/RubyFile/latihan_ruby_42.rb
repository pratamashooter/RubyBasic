# Hash 

# Cara penulisan dan cara pemanggilan ke 1
siswa = {'nama' => 'Budi', 'umur' => 17, 'rajin' => true}
puts siswa['nama']

# Cara penulisan dan cara pemanggilan ke 2    => Key
siswa = {:nama => 'Budi', :umur => 17, :rajin => true}
puts siswa[:nama]

# Cara penulisan dan cara pemanggilan ke 3
siswa = {nama: 'Budi', umur: 17, rajin: true}
puts siswa[:nama]